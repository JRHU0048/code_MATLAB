%% 矩阵的创建方法：
（1）直接输入；（2）函数创建（3）本地文件导入

第一组函数： zeros、ones 和 eye。这三个函数分别用来创建全为 0 的矩阵、全为 1 的矩阵和单位矩阵
第二组函数： rand、randi 和 randn。这三个函数分别用来创建均匀分布的随机数、均匀分布的随机整数和标准正态分布的随机数
