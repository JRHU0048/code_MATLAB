%% 常见的数学运算函数
abs  求绝对值
floor  朝负无穷大四舍五入
fix    朝零四舍五入，相当于舍去小数部分
ceil   朝正无穷大四舍五入
round  真正意义上的四舍五入函数（将结果四舍五入为最近的整数，如果为 0.5，则会朝着偏离零的方向调整）
    round 函数还有第二种用法，它可以输入第二个参数：
    round(X,N)可以将 X 在第 N 位数四舍五入，分下面三种情况：
    1) N > 0：四舍五入到小数点右侧的第 N 位数。
    2) N = 0：四舍五入到最接近的整数。
    3) N < 0：四舍五入到小数点左侧的第 N 位数。
    只有一个输入参数的用法：
        round(1.1) % 1
        round(1.9) % 2
        round(-1.1) % -1
        round(-1.9) % -2
        round(1.5) % 2
        round(-1.5) % -2
        有两个输入参数的用法：
        round(3.14159, 1) % 3.1
        round(3.14159, 3) % 3.142
        round(3.14159, 0) % 3
        round(12345.6, -1) % 12350
        round(12345.6, -2) % 12300
        round(12345.6, -3) % 12000
mod   mod(a,m)可以计算 a 除以 m 后的余数，其中 a 是被除数，m 是除数。
sqrt  sqrt(a)可以计算 a 的平方根，即对a 开根号。如果 a 为负数则返回复数结果。其结果和 a^(1/2)等价。
exp   exp 函数可以计算以自然常数 e 为底的指数。 exp(1) = 2.7183
 




