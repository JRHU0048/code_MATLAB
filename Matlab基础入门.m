（3）脚本文件命名：以字母开头，可以包含字母、数字、下划线，以.m 后缀
（4）使用记事本打开写好的脚本文件比较方便
（5）点击运行，运行当前目录下的脚本文件
（6）右键——清除工作区
（7）tips 高亮显示当前行、行号、数据提示

