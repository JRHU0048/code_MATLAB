%% 变量
Matlab在使用变量时不需要提前声明变量的类型和具体数值，在具体的运行中直接执行；
（1）变量名以字母开头，后面可以是字母，数字，下划线
（2）变量名区分大小写
（3）不能超过63个字符，第63个字符被忽略
（4）不能与MATLAB关键字同名

