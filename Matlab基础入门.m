MATLAB中的向量：向量可以看成一维数组   多维数组：两个以上的维度，一般不会用到
%% 向量的创建方法：
（1）元素较少，直接输入
    a=[1,3,5] a=[1 3 5]  行向量
    a=[1;3;5]   列向量
    或a=[1
    3
    5]
（2）冒号法：A:step:B
    A是起始值，step是每次递增或递减的步长，B是终止值（不一定刚好停在这里）
    1:2:7->[1,3,5,7]
    1:2:8->[1,3,5,7]
    1:2:9->[1,3,5,7,9]
    2:3:18->[2,5,8,11,14,17]
    0:0.1:1->[0,0.1,0,2,...0.99，1]
    1:1:10->[1,2,3,4,...]  等价于1:10 %步长为1时可以省略
（3）利用函数来创建：
    linspace函数创建等差数列
    logspace函数创建等比数列
    
linspace 函数，它有两种用法，区别在于是否给定第三个输入参数 n，如果我们不指定 n，则 MATLAB 会默认 n=100
    linspace(a,b): 该命令用来创建一个行向量，向量中的第一个元素为 a，最后一个元素为 b，形成总数为 100 个元素的线性间隔的向量（默认n为100）
    linspace (a,b,n): 该命令用来创建一个行向量，向量中的第一个元素为 a，最后一个元素为 b，形成总数为 n 个元素的线性间隔的向量

