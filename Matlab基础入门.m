%% 计算矩阵的大小
使用size函数，size(A),返回矩阵A的维度；
A 是一个三行四列的矩阵；
[r,c] = size(A) r表示行数，c表示列数；
size(A,dim)返回在维度 dim 上的长度。dim = 1 时表示行；dim = 2 时表示列。若A是一个3×4 的矩阵，则 size(A,1)返回 3，size(A,2)返回 4。
（length 函数和 numel 函数也可以用在矩阵上。length 函数会返回行和列的较大值：对上面的 A 矩阵，length(A)返回 4；numel 函数会返回矩阵中元素的总数，numel(A) 返回 12）

sub2ind 将矩阵的下标转换为线性索引；ind2sub 将线性索引转换为下标


